async def main():
    print("=" * 60)
    print("开始批量生成A号8张轮播图")
    print("=" * 60)

    generator = GeminiAPIGenerator(model="pro")
    await generator.start()

    results = []
    for i, item in enumerate(PROMPTS, 1):
        print(f"\n[{i}/8] 生成: {item['name']}")
        try:
            result = await generator.generate_image(
                prompt=item["prompt"],
                account="A",
                page_type=item["name"]
            )
            results.append({"name": item["name"], "success": result is not None, "path": result})
            if result:
                print(f"  [OK] 保存到: {result}")
            else:
                print(f"  [FAIL] 生成失败")
        except Exception as e:
            print(f"  [ERROR] {e}")
            results.append({"name": item["name"], "success": False, "error": str(e)})

        # 间隔5秒避免频率限制
        if i < len(PROMPTS):
            print("  等待5秒...")
            await asyncio.sleep(5)

    # 打印汇总
    print("\n" + "=" * 60)
    print("生成完成汇总")
    print("=" * 60)
    success_count = sum(1 for r in results if r["success"])
    print(f"成功: {success_count}/8")

    for r in results:
        icon = "[OK]" if r["success"] else "[X]"
        print(f"  {icon} {r['name']}")

    await generator.close()


if __name__ == "__main__":
    asyncio.run(main())
